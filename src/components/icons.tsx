// Helper to get icon by category name
export function getCategoryIcon(category: string, className = "size-6") {
  const iconMap: Record<string, React.FC<{ className?: string }>> = {
    "إلكترونيات": ElectronicsIcon,
    "برامج وتطبيقات": SoftwareIcon,
    "ملابس وإكسسوارات": ClothingIcon,
    "أدوات ومعدات": ToolsIcon,
    "كتب ودورات": BooksIcon,
    "خدمات رقمية": DigitalServicesIcon,
  };
  const IconComponent = iconMap[category] || ShoppingBagIcon;
  return <IconComponent className={className} />;
}
